package com.eg.constructors;

public class ParentClass {
	String name;

	public ParentClass() {
		this.name="TARA";
		System.out.println("In the parent class constructor");
	}

	public void Hello() {
		System.out.println("In the hello method of super-class(parent)");

	}

	public void HelloWorld() {
		System.out.println("In the helloworld method of super-class(parent)");

	}
}
